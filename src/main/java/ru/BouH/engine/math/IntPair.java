package ru.BouH.engine.math;

public final class IntPair {
    private int a1;
    private int a2;

    public IntPair(int a1, int a2) {
        this.a1 = a1;
        this.a2 = a2;
    }

    public int getA1() {
        return this.a1;
    }

    public void setA1(int a1) {
        this.a1 = a1;
    }

    public int getA2() {
        return this.a2;
    }

    public void setA2(int a2) {
        this.a2 = a2;
    }
}
